function simulate!(env::AbstractEnv, policy::Function)
    reset!(env)
    s = observe(env)
    hist = []
    while !terminated(env)
        a = policy(s)
        r = act!(env, a)
        sp = observe(env)
        push!(hist, (s=s, a=a, r=r, sp=sp))
        s = sp
    end
    return hist
end

